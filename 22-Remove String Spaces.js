//  fungsi yang menghilangkan spasi dari string,
//  lalu kembalikan string yang dihasilkan.
function noSpace(x) {
  return x.split(" ").join("");
}
console.log(noSpace("8 8 Bi fk8h B 8 BB8B B B  B888 c hl8 BhB fd"));
